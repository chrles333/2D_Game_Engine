Map::Map() {
	// Load map textures for now
	dirt = TextureManager::LoadTexture("assets/dirt.png");
	grass = TextureManager::LoadTexture("assets/grass.png");
	water = TextureManager::LoadTexture("assets/water.png");

	// Load default map
	loadMap(lvl1);

	src.x = src.y = 0;
	src.w = dest.w = 32;
	src.h = dest.h = 32;

	// To make sure we dont stop at some random int
	dest.x = dest.y = 0;
}

void Map::loadMap(int arr[20][25]) {
	// Copy map tiles
	for (int row = 0; row < 20; ++row) {
		for (int col = 0; col < 25; ++col) {
			map[row][col] = arr[row][col];
		}
	}
}

void Map::drawMap() {
	int type = 0;

	for (int row = 0; row < 20; ++row) {
		for (int col = 0; col < 25; ++col) {
			// Check type of current map tile
			type = map[row][col];

			// Move by 32 pixels
			dest.x = col * 32;
			dest.y = row * 32;

			switch (type) {
			// Water
			case 0:
				TextureManager::Draw(water, src, dest);
				break;
			case 1:
				TextureManager::Draw(grass, src, dest);
				break;
			case 2:
				TextureManager::Draw(dirt, src, dest);
				break;
			default:
				break;
			}
		}
	}
}